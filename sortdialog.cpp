#include "SortDialog.h"
#include <QVBoxLayout>
#include <QHBoxLayout>
#include <QGroupBox>

// ============================================================================
// Конструктор и деструктор
// ============================================================================

SortDialog::SortDialog(QWidget* parent)
    : QDialog(parent)
{
    setupUI();                          // Создаем интерфейс
    setWindowTitle("Сортировка решений"); // Устанавливаем заголовок окна
    setModal(true);                      // Модальное окно (блокирует основное окно)
    resize(400, 150);                    // Устанавливаем размер окна
}

SortDialog::~SortDialog()
{
    // Деструктор - ничего особенного не требуется
}

// ============================================================================
// Инициализация интерфейса
// ============================================================================

/**
 *  Создает и настраивает пользовательский интерфейс диалога
 *
 * Структура интерфейса:
 * ┌─────────────────────────────────────────────┐
 * │  Сортировка решений                          │
 * ├─────────────────────────────────────────────┤
 * │  Порядок сортировки по весу                  │
 * │  ○ По возрастанию (от легких к тяжелым)     │
 * │  ○ По убыванию (от тяжелых к легким)        │
 * ├─────────────────────────────────────────────┤
 * │  [ Сортировать ]    [ Отмена ]              │
 * └─────────────────────────────────────────────┘
 */
void SortDialog::setupUI()
{
    // Основной вертикальный layout
    QVBoxLayout* mainLayout = new QVBoxLayout(this);

    // ========================================================================
    // Группа с выбором порядка сортировки
    // ========================================================================
    QGroupBox* sortGroup = new QGroupBox("Порядок сортировки по весу", this);
    QVBoxLayout* sortLayout = new QVBoxLayout(sortGroup);

    // Radio-кнопка "По возрастанию" (выбрана по умолчанию)
    m_ascendingRadio = new QRadioButton("По возрастанию (от легких к тяжелым)", this);

    // Radio-кнопка "По убыванию"
    m_descendingRadio = new QRadioButton("По убыванию (от тяжелых к легким)", this);

    // Устанавливаем сортировку по возрастанию как вариант по умолчанию
    m_ascendingRadio->setChecked(true);

    // Добавляем radio-кнопки в layout группы
    sortLayout->addWidget(m_ascendingRadio);
    sortLayout->addWidget(m_descendingRadio);

    mainLayout->addWidget(sortGroup);

    // ========================================================================
    // Кнопки управления
    // ========================================================================
    QHBoxLayout* buttonLayout = new QHBoxLayout();

    m_sortButton = new QPushButton("Сортировать", this);
    m_cancelButton = new QPushButton("Отмена", this);

    buttonLayout->addWidget(m_sortButton);
    buttonLayout->addWidget(m_cancelButton);

    mainLayout->addLayout(buttonLayout);

    // ========================================================================
    // Подключение сигналов
    // ========================================================================

    // При нажатии "Сортировать" вызываем onSortClicked()
    connect(m_sortButton, &QPushButton::clicked, this, &SortDialog::onSortClicked);

    // При нажатии "Отмена" просто закрываем диалог
    connect(m_cancelButton, &QPushButton::clicked, this, &QDialog::reject);
}

// ============================================================================
// Обработчики событий
// ============================================================================

/**
 *  Обработчик нажатия кнопки "Сортировать"
 *
 * Действия:
 * 1. Отправляет сигнал sortRequested с выбранным направлением сортировки
 * 2. Закрывает диалог с кодом accept() (успешное завершение)
 *
 * Сигнал sortRequested будет обработан в MainWindow::onSolutionsSorted()
 */
void SortDialog::onSortClicked()
{
    // Отправляем сигнал с выбранным направлением сортировки
    // isAscending() вернет true, если выбрана сортировка по возрастанию
    emit sortRequested(isAscending());

    // Закрываем диалог с успешным кодом возврата
    accept();
}